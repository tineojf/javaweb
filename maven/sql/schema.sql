CREATE TABLE ASOCIADOS (
    CODIGO INT PRIMARY KEY AUTO_INCREMENT,
    NOMBRE VARCHAR(50) NOT NULL,
    APELLIDOS VARCHAR(100) NOT NULL,
    ESTADO_CIVIL VARCHAR(20) DEFAULT 'Soltero',
    EPS VARCHAR(50) DEFAULT 'No Definido',
    DNI VARCHAR(20) NOT NULL UNIQUE,
    APORTES DOUBLE DEFAULT 0,
    NIVEL_ESTUDIO VARCHAR(50) DEFAULT 'No Definido',
    SALARIO DOUBLE NOT NULL,
    NUMERO_HIJOS INT DEFAULT 0,
    TELEFONO VARCHAR(20) NOT NULL,
    DIRECCION VARCHAR(100) DEFAULT 'No Definido'
);

